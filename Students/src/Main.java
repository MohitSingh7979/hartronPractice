    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement ps = con.prepareStatement("SELECT subject_id FROM mst_subject where subject_name = ?");
            ps.setString(1, subject.getSelectedItem().toString());
            ResultSet rs = ps.executeQuery();
            rs.next();

            int id = Integer.parseInt(studentId.getText());
            String stName = name.getText();
            int sid = rs.getInt(1);
            int m1 = Integer.parseInt(mark1.getValue().toString());
            int m2 = Integer.parseInt(mark2.getValue().toString());

            PreparedStatement ps1 = con.prepareStatement("INSERT INTO `school`.`tbl_marks` (`id`, `name`, `subject_id`, `mark1`, `mark2`) VALUES (?,?,?,?,?);");
            ps1.setInt(1, id);
            ps1.setString(2, stName);
            ps1.setInt(3, sid);
            ps1.setInt(4, m1);
            ps1.setInt(5, m2);
            ps1.execute();
            JOptionPane.showMessageDialog(rootPane, "values inserted");

            showReport1();
            showReport2();
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

