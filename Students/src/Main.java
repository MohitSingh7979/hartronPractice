    private void showReport2() {
        try {
            PreparedStatement ps = con.prepareStatement("SELECT mst_subject.subject_name, COUNT(*),mark1,mark2 FROM tbl_marks join mst_subject on tbl_marks.subject_id = mst_subject.subject_id GROUP BY tbl_marks.subject_id");

            ResultSet rs = ps.executeQuery();
            int m1 = rs.getInt("mark1");
            int m2 = rs.getInt("mark2");

            DefaultTableModel m = (DefaultTableModel) table2.getModel();
            m.setRowCount(0);

            while (rs.next()) {
                Vector data = new Vector();
                data.add(rs.getString(1));
                data.add(rs.getString(2));

                m.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void showReport1() {
        try {
            PreparedStatement ps = con.prepareStatement("select id, name, (SELECT subject_name FROM mst_subject where subject_id = tbl_marks.subject_id), mark1, mark2 from tbl_marks");
            ResultSet rs = ps.executeQuery();

            DefaultTableModel m = (DefaultTableModel) table1.getModel();
            m.setRowCount(0);

            while (rs.next()) {
                Vector data = new Vector();
                data.add(rs.getString(1));
                data.add(rs.getString(2));
                data.add(rs.getString(3));
                data.add(rs.getString(4));
                data.add(rs.getString(5));
                
           
                m.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void showStudents() {
        try {
            PreparedStatement ps = con.prepareStatement("SELECT subject_name FROM mst_subject");
            ResultSet rs = ps.executeQuery();
            subject.removeAllItems();
            while (rs.next()) {
                subject.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

